import { FRPRPGScenario } from "./frpMechanics";

export interface FRPMechanics {
  npcInteractions: NPCInteraction[];
  decisionPoints: DecisionPoint[];
  combatSystem: CombatSystem;
  diceMechanics: DiceMechanics;
}

export interface NPCInteraction {
  id: string;
  name: string;
  dialogue: string;
  personality: string;
  role: string;
  dialogueOptions: DialogueOption[];
  consequences: Consequence[];
}

export interface DialogueOption {
  text: string;
  requiredSkill?: string;
  requiredStat?: string;
  diceRoll?: string;
  consequences: Consequence;
}

export interface Consequence {
  immediate: string;
  longTerm: string;
  skillCheck?: {
    skill: string;
    difficulty: number;
    success: string;
    failure: string;
  };
}

export interface DecisionPoint {
  id: string;
  title: string;
  description: string;
  choices: Choice[];
  requiredSkill?: string;
  requiredStat?: string;
  diceRoll?: string;
}

export interface Choice {
  text: string;
  requiredSkill?: string;
  requiredStat?: string;
  diceRoll?: string;
  consequences: Consequence;
  nextScene?: string;
}

export interface CombatSystem {
  initiativeSystem: InitiativeSystem;
  combatActions: CombatAction[];
  statusEffects: StatusEffect[];
  victoryConditions: VictoryCondition[];
  defeatConditions: DefeatCondition[];
  rewards: Reward[];
  penalties: Penalty[];
}

export interface InitiativeSystem {
  order: string[]; // Character IDs in initiative order
  currentRound: number;
  currentTurn: number;
  roundActions: RoundAction[];
}

export interface RoundAction {
  round: number;
  turn: number;
  characterId: string;
  action: CombatAction;
  diceRoll?: DiceRoll;
  result: string;
  statusEffectsApplied: StatusEffect[];
}

export interface CombatAction {
  id: string;
  name: string;
  type: "attack" | "defend" | "special" | "item" | "spell";
  description: string;
  diceRoll: DiceRoll;
  damageCalculation: DamageCalculation;
  statusEffects?: StatusEffect[];
  cooldown?: number;
  requirements?: {
    skill?: string;
    stat?: string;
    item?: string;
  };
}

export interface DiceRoll {
  diceType: string; // 'd6', 'd20', etc.
  numberOfDice: number;
  modifier: number;
  targetNumber?: number;
  advantage?: boolean;
  disadvantage?: boolean;
}

export interface DamageCalculation {
  baseDamage: number;
  diceRoll: DiceRoll;
  modifiers: {
    strength?: number;
    weapon?: number;
    magic?: number;
    critical?: number;
  };
  statusEffects?: StatusEffect[];
}

export interface StatusEffect {
  id: string;
  name: string;
  type: "buff" | "debuff" | "neutral";
  description: string;
  duration: number; // rounds
  effects: {
    hp?: number;
    attack?: number;
    defense?: number;
    speed?: number;
    accuracy?: number;
    resistance?: string[];
  };
  visualEffect?: string;
  removalCondition?: string;
}

export interface VictoryCondition {
  id: string;
  type:
    | "defeat_all_enemies"
    | "reach_location"
    | "complete_objective"
    | "survive_rounds";
  description: string;
  requirements: string[];
}

export interface DefeatCondition {
  id: string;
  type: "party_defeated" | "time_limit" | "objective_failed";
  description: string;
  requirements: string[];
}

export interface Reward {
  id: string;
  type: "experience" | "gold" | "item" | "skill_point" | "stat_increase";
  value: number;
  description: string;
}

export interface Penalty {
  id: string;
  type: "damage" | "status_effect" | "item_loss" | "stat_decrease";
  value: number;
  description: string;
}

export interface DiceMechanics {
  diceTypes: string[];
  customDice: CustomDice[];
  probabilityTables: ProbabilityTable[];
  criticalRules: {
    natural20: string;
    natural1: string;
    criticalRange: number[];
  };
}

export interface CustomDice {
  name: string;
  sides: number;
  faces: string[];
  specialRules: string[];
}

export interface ProbabilityTable {
  name: string;
  diceRoll: DiceRoll;
  outcomes: {
    range: [number, number];
    result: string;
    probability: number;
  }[];
}

export interface FRPRPGScenario {
  title: string;
  theme: string;
  difficulty: string;
  scenes: FRPScene[];
  npcs: NPCInteraction[];
  combatEncounters: CombatEncounter[];
  diceMechanics: DiceMechanics;
}

export interface FRPScene {
  id: string;
  title: string;
  description: string;
  npcs: NPCInteraction[];
  decisionPoints: DecisionPoint[];
  combatEncounter?: CombatEncounter;
  rewards: Reward[];
  nextScenes: string[];
  environment: {
    name: string;
    description: string;
    effects: StatusEffect[];
    hazards: string[];
  };
}

export interface CombatEncounter {
  id: string;
  title: string;
  description: string;
  enemies: Enemy[];
  environment: {
    name: string;
    description: string;
    effects: StatusEffect[];
    hazards: string[];
  };
  initiativeSystem: InitiativeSystem;
  victoryConditions: VictoryCondition[];
  defeatConditions: DefeatCondition[];
  rewards: Reward[];
  penalties: Penalty[];
  rounds: CombatRound[];
}

export interface Enemy {
  id: string;
  name: string;
  hp: number;
  maxHp: number;
  attack: number;
  defense: number;
  speed: number;
  initiative: number;
  specialAbilities: CombatAction[];
  statusEffects: StatusEffect[];
  loot: Reward[];
  experience: number;
  description: string;
  aiBehavior: {
    aggressive: boolean;
    defensive: boolean;
    tactical: boolean;
    specialAbilityUsage: string;
  };
}

export interface CombatRound {
  roundNumber: number;
  turns: CombatTurn[];
  statusEffects: StatusEffect[];
  environmentalEffects: StatusEffect[];
}

export interface CombatTurn {
  turnNumber: number;
  characterId: string;
  action: CombatAction;
  diceRoll: DiceRoll;
  result: string;
  damageDealt?: number;
  statusEffectsApplied: StatusEffect[];
  statusEffectsRemoved: StatusEffect[];
}

export interface SkillTree {
  id: string;
  name: string;
  description: string;
  class: string;
  skills: SkillNode[];
  maxSkillPoints: number;
  currentSkillPoints: number;
}

export interface SkillNode {
  id: string;
  name: string;
  description: string;
  icon: string;
  level: number;
  maxLevel: number;
  cost: number;
  requirements: SkillRequirement[];
  effects: SkillEffect[];
  isUnlocked: boolean;
  isMaxed: boolean;
}

export interface SkillRequirement {
  type: "level" | "skill" | "stat";
  value: string | number;
  description: string;
}

export interface SkillEffect {
  type: "stat_boost" | "ability_unlock" | "passive_effect" | "active_ability";
  target: string;
  value: number;
  description: string;
}

